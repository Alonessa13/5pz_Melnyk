# Запитуємо у користувача дані про кандидата
age = int(input("Введіть вік кандидата: "))
experience = int(input("Введіть кількість років досвіду: "))
qualification = input("Чи є у кандидата спеціальна кваліфікація? (так/ні): ")

# Конвертуємо відповідь у булеве значення
has_qualification = qualification.lower() == "так"

# Перевіряємо, чи відповідає кандидат вимогам
if 25 <= age <= 50 and (experience >= 3 or has_qualification):
    print("Кандидат відповідає вимогам")
else:
    print("Кандидат не відповідає вимогам")
